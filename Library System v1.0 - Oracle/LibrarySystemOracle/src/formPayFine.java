    private void btnPayFineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayFineActionPerformed
        DAO_LoanRecord dao = new DAO_LoanRecord();
        String loanRecord = txtLoanRecord.getText();
        String isbn = txtIsbn.getText();
        DecimalFormat df = new DecimalFormat();
        df.setMaximumFractionDigits(2);
        double amountPaid = Double.parseDouble(txtAmountPaid.getText());
        df.format(amountPaid);
        dao.payFine(loanRecord, isbn, amountPaid);
        txtAmountPaid.setText("");
        txtIsbn.setText("");
        txtLoanRecord.setText("");
        JOptionPane.showMessageDialog(null, "Your fine has been successfully processed", "Fine Processed: ", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnPayFineActionPerformed

